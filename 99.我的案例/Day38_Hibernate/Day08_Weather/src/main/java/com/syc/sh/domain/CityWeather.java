package com.syc.sh.domain;

public class CityWeather {

	private int id;
	private String country;
	private String city;
	private String recordDate;
	private String weather;
	private String wind;
	private String temperature;
	private String threeClock;
	private String sixClock;
	private String nineClock;
	private String twelveClock;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getRecordDate() {
		return recordDate;
	}

	public void setRecordDate(String recordDate) {
		this.recordDate = recordDate;
	}

	public String getWeather() {
		return weather;
	}

	public void setWeather(String weather) {
		this.weather = weather;
	}

	public String getWind() {
		return wind;
	}

	public void setWind(String wind) {
		this.wind = wind;
	}

	public String getTemperature() {
		return temperature;
	}

	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}

	public String getThreeClock() {
		return threeClock;
	}

	public void setThreeClock(String threeClock) {
		this.threeClock = threeClock;
	}

	public String getSixClock() {
		return sixClock;
	}

	public void setSixClock(String sixClock) {
		this.sixClock = sixClock;
	}

	public String getNineClock() {
		return nineClock;
	}

	public void setNineClock(String nineClock) {
		this.nineClock = nineClock;
	}

	public String getTwelveClock() {
		return twelveClock;
	}

	public void setTwelveClock(String twelveClock) {
		this.twelveClock = twelveClock;
	}

}
